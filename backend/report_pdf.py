"""
Build a minimal PDF health report using fpdf2 (student-friendly API).
"""
from __future__ import annotations

from typing import Any

from fpdf import FPDF


def build_health_report_pdf(data: dict[str, Any]) -> bytes:
    pdf = FPDF()
    pdf.set_auto_page_break(auto=True, margin=15)
    pdf.add_page()
    pdf.set_font("Helvetica", "B", 16)
    pdf.cell(0, 10, "PredictAI - Health Report (Educational)", ln=True)
    pdf.ln(4)
    pdf.set_font("Helvetica", size=11)
    pdf.multi_cell(0, 6, "Disclaimer: This report is generated by a student project for demonstration only. It is not medical advice.")
    pdf.ln(4)

    def section(title: str, body: str) -> None:
        pdf.set_font("Helvetica", "B", 12)
        pdf.cell(0, 8, title, ln=True)
        pdf.set_font("Helvetica", size=11)
        pdf.multi_cell(0, 6, body)
        pdf.ln(2)

    section("Symptoms (raw)", data.get("symptom_text") or "-")

    matched = data.get("matched_symptoms") or []
    section("Mapped symptoms", ", ".join(s.replace("_", " ") for s in matched) or "-")

    preds = data.get("predictions") or []
    lines = []
    for p in preds:
        lines.append(f"- {p.get('disease', '?')}: {float(p.get('probability', 0)):.1f}%")
    section("Top predictions", "\n".join(lines) if lines else "-")

    section("Severity", str(data.get("severity") or "-"))
    if data.get("doctor_warning"):
        section("Alert", "Please consult a doctor immediately (many symptoms reported).")

    bmi = data.get("bmi") or {}
    if bmi:
        bmi_line = f"Value: {bmi.get('value', '-')}, Category: {bmi.get('category', '-')}"
        section("BMI", bmi_line)

    diet = data.get("diet") or {}
    eat = diet.get("foods_eat") or diet.get("foods") or []
    avoid = diet.get("foods_avoid") or []
    if eat or avoid:
        section("Diet - prefer", "\n".join(f"- {x}" for x in eat) or "-")
        section("Diet - limit / avoid", "\n".join(f"- {x}" for x in avoid) or "-")

    tips = data.get("suggestions") or []
    if tips:
        section("Lifestyle tips", "\n".join(f"- {t}" for t in tips))

    # fpdf2: dest='S' returns PDF as bytes / bytearray
    raw = pdf.output(dest="S")
    if isinstance(raw, str):
        return raw.encode("latin-1")
    return bytes(raw)
